#include "COTS/4-LIB/STD_TYPES.h"
#include "COTS/2-HAL/3-LCD/CLCD_interface.h"
#include "avr/delay.h"

void main(void){
	CLCD_voidInit();

	u8 letter1arr[8]={
			0b11000,
			0b11010,
			0b00010,
			0b11110,
			0b11100,
			0b11100,
			0b10100 };
	u8 letter2arr[8]={
			0b11000,
			0b11001,
			0b00001,
			0b11110,
			0b11100,
			0b11100,
			0b10100 };






	while(1){
		CLCD_voidWriteSpecialCharacter(&letter1arr,0,0,0);
		_delay_ms(200);
		CLCD_voidWriteSpecialCharacter(&letter2arr,1,0,0);
		_delay_ms(200);
	}
}
