#include "COTS/1-MCAL/1-DIO/DIO_interface.h"
#include "COTS/2-HAL/1-SW/SW_interface.h"



void main(void){
	DIO_u8SetPinDirection(DIO_enumPORTB,DIO_enumPIN1,DIO_enumPIN_OUTPUT);
	SW_str SW1 ={SW_enumPORTB,SW_enumPIN0,SW_enumPullUpInternal};
	SW_voidInit(&SW1);
	while(1){
		if (SW_enumGetValue(&SW1)== SW_enumON)
		{
			DIO_u8SetPinValue(DIO_enumPORTB,DIO_enumPIN1,DIO_enumPIN_HIGH);
		}else{
			DIO_u8SetPinValue(DIO_enumPORTB,DIO_enumPIN1,DIO_enumPIN_LOW);
		}
	}
}
