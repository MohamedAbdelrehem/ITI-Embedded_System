#include "COTS/4-LIB/STD_TYPES.h"
#include "COTS/2-HAL/3-LCD/CLCD_interface.h"
#include "avr/delay.h"

void main(void){
	CLCD_voidInit();




	while(1){
		for(int y=0;y<16;y++){
			for(int x=0;x<2;x++){
				CLCD_voidGoToXY(x,y);
				CLCD_voidSendString("Mohamed");
				y++;
				_delay_ms(100);
				CLCD_voidSendCommand(1);
			}
		}
	}
}
